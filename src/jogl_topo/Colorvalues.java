package jogl_topo;

public class Colorvalues {
	public float parse;
	public float parse1;
	public float parse2;
	public int radian;

	public Colorvalues() {
		parse = 0;
		parse1 = 0;
		parse2 = 0;
		radian = 1;
	}

	public void setColorvalues(float i, float j, float k) {
		parse = i;
		parse1 = j;
		parse2 = k;
	}
}